package abstracts;

public abstract class AnimalAB {
    private String nome;
    private int idade;

    public abstract void emitirSom();

    public AnimalAB(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}